public class AsciiArt {

    /**
     * Prints my name in ASCII Art to the console.
     * @param args Comand-Line arguments are ignored.
     */

    public static void main(String[] args){
        System.out.println("____   ____     ____    ____          ________");
        System.out.println("I  \\  /   I     I  I    I  I  ____    I   ____|");
        System.out.println("I   \\/    I     I__I    I  I /   /    I  I");
        System.out.println("I         I     ____    I   /   /     I  I ");
        System.out.println("I  I\\/I   I     I  I    I      /      I  I__");
        System.out.println("I  I  I   I     I  I    I     /       I   __|");
        System.out.println("I  I  I   I     I  I    I      \\      I  I");
        System.out.println("I  I  I   I     I  I    I  I\\   \\     I  I____");
        System.out.println("I__I  I___I     I__I    I__I \\___\\    I_______|");

    }
}
